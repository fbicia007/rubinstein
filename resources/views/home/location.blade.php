                                    <h4>Düsseldorf, Deutschland</h4>
                                    <span class="story-content">
								<p>Stammsitz der Internationalen Musikakademie Anton Rubinstein ist Düsseldorf, die Landeshauptstadt des bevölkerungsreichsten deutschen Bundeslands Nordrhein-Westfalen. Die Rhein-Ruhr-Region wartet mit einer weltweit kaum vergleichbaren Dichte an kulturellen Einrichtungen wie Theatern, Opernhäusern, Orchestern oder Museen auf.</p>
<p>Die Räumlichkeiten der Musikakademie befinden sich im denkmalgeschützten barocken Haus „Goldener Helm“ im Herzen der lebendigen Düsseldorfer Altstadt. Die großen Musikinstitutionen der rheinischen Metropole wie die Tonhalle Düsseldorf oder die Deutsche Oper am Rhein, Konzertsäle wie das Palais Wittgenstein und der Robert Schumann Saal sind ebenso wie die bedeutenden Kunstmuseen von hier aus in wenigen Minuten zu Fuß erreichbar.</p>
<p>Seit ihrer Gründung im Jahr 2002 arbeitet die Internationale Musikakademie Anton Rubinstein eng mit dem Steinway Haus Düsseldorf zusammen, das der Musikakademie hochwertige Instrumente für die Wettbewerbe und die Klassischen Soireen auf der Kö zur Verfügung stellt.</p>
<p>Der Heinrich-Heine-Universität Düsseldorf ist die Internationale Musikakademie Anton Rubinstein durch die Konzertreihe “Rubinstein Showcase” im Haus der Universität am Schadowplatz verbunden.</p>
