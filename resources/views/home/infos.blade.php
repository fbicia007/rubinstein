                            <div class='content-column one_half'>
                                <p>安东·鲁宾斯坦国际音乐学院致力于为有才华的年轻音乐家提供个性化的学习机会。 我们提供广泛的学习选择：包含全日制学习和具有国家认证的研究生课程以及私人课程和大师班。 我们的目标是在多样化和个性化的氛围中提供具有国际竞争力的专业课程。</p>
                                <p>本校教师成员均由国家级院团首席乐器演奏家和德国最具实力的极富演奏魅力的青年乐器演奏家组成，具有丰富的国际音乐会演出及教学经验，在每一个学生的音乐成长之路上给予陪同并提供专业的艺术指导。</p>
                                <p>我们将举办多种活动，包含定期的音乐会及国际音乐比赛，让每一位学生都能得到丰富的舞台演出经验及多种交流学习机会。</p>
                                <p>在安东·鲁宾斯坦国际音乐学院毕业后我们将授予毕业证书。此外我们与意大利拉斯佩齐亚市的贾科莫·普契尼音乐学院（Conservatorio Giacomo Puccini - La Spezia ），荷兰荷兰方提斯艺术学院（Fontys School of Fine and Performing Arts）以及瑞士的Kalaidos大学（Kalaidos Universität in Zürich/Schweiz）合作，提供德国国家认可的硕士和硕士II（室内乐团）课程。</p>
                            </div>
