                                    <h4>Berlin, Deutschland</h4>
                                    <span class="story-content">
								<p>Seit 2016 ist die Internationale Musikakademie Anton Rubinstein auch in Berlin, Hauptstadt der Bundesrepublik Deutschland und eine der lebendigsten Musikmetropolen der Welt, vertreten.  Nur etwa 50 Meter vom Kurfürstendamm entfernt, mitten in Charlottenburg, stehen drei repräsentative Räume mit etwa fünf Meter hohen Decken und jeweils einem Flügel für den Unterricht zur Verfügung.</p>
<p>An unserem Berliner Standort sind alle Studienformen und Abschlüsse (Akademiestudium, Bachelor und Master of Music, Konzertexamen und Vorstudium) möglich.</p>
<p>Zur Zeit unterrichten wir dort die Fächer Violine, Violoncello, Gesang, Flöte und Klavier. Die Erweiterung auf unser gesamtes Spektrum an Fachrichtungen ist in der Planung – sprechen Sie uns daher gerne auf Ihre Wünsche an!</p>
