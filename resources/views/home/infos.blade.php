                                <p>此项双民族合作课程在2014年被德国和意大利经济协会授予“德意跨文化成果” 特别奖。</p>
                                <p>音乐学院坐落于杜塞尔多夫老城区中心地带，校舍建筑为巴洛克时期风格，同时具有“金头盔”的别名。在学院步行距离内 有多所杜塞尔多夫市在莱茵河畔建立了众多音乐机构及演奏厅，例如德意志歌剧院，维特根斯音乐厅，罗伯特舒曼音乐厅以及众多艺术博物馆。</p>
